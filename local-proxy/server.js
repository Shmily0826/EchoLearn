async function fetchViaInnerTube(videoId, lang) {
  const clients = [
    { name: 'ANDROID', clientVersion: '20.10.38', ua: ANDROID_UA },
    { name: 'WEB', clientVersion: '2.20241201.00.00', ua: BROWSER_UA },
  ];

  for (const client of clients) {
    try {
      const context = {
        clientName: client.name,
        clientVersion: client.clientVersion,
        hl: lang,
      };
      if (client.name === 'WEB') context.userAgent = BROWSER_UA;

      const resp = await fetch(INNERTUBE_API_URL, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'User-Agent': client.ua,
          Cookie: CONSENT_COOKIE,
        },
        body: JSON.stringify({
          context: { client: context },
          videoId,
          contentCheckOk: true,
          racyCheckOk: true,
        }),
      });

      if (!resp.ok) continue;

      const data = await resp.json();
      if (data.playabilityStatus?.status !== 'OK') continue;

      const tracks =
        data?.captions?.playerCaptionsTracklistRenderer?.captionTracks;
      if (!Array.isArray(tracks) || tracks.length === 0) continue;

      return await fetchFromTracks(tracks, lang);
    } catch {
      continue;
    }
  }
  return null;
}

// ── Web page scraping strategy ─────────────────────────────────

async function fetchViaWebPage(videoId, lang) {
  const pageUrl = `https://www.youtube.com/watch?v=${videoId}`;
  const resp = await fetch(pageUrl, {
    headers: {
      'User-Agent': BROWSER_UA,
      'Accept-Language': 'en-US,en;q=0.9',
      Accept: 'text/html,application/xhtml+xml',
      Cookie: CONSENT_COOKIE,
    },
    redirect: 'follow',
  });

  if (!resp.ok) return null;

  const html = await resp.text();

  // Check for CAPTCHA
  if (
    html.includes('class="g-recaptcha"') ||
    html.includes('id="captcha-form"') ||
    html.includes('<title>Sorry')
  ) {
    return null;
  }

  // Extract ytInitialPlayerResponse
  const patterns = [
    'var ytInitialPlayerResponse = ',
    'ytInitialPlayerResponse = ',
  ];

  for (const token of patterns) {
    const idx = html.indexOf(token);
    if (idx < 0) continue;

    const jsonStart = idx + token.length;
    if (html[jsonStart] !== '{') continue;

    const jsonStr = extractJsonObject(html, jsonStart);
    if (!jsonStr) continue;

    try {
      const playerResponse = JSON.parse(jsonStr);
      const tracks =
        playerResponse?.captions?.playerCaptionsTracklistRenderer?.captionTracks;
      if (!Array.isArray(tracks) || tracks.length === 0) continue;

      return await fetchFromTracks(tracks, lang);
    } catch {
      continue;
    }
  }

  return null;
}

// ── Caption track fetching & parsing ───────────────────────────

async function fetchFromTracks(tracks, lang) {
  const manual = tracks.find((t) => t.languageCode === lang && t.kind !== 'asr');
  const auto = tracks.find((t) => t.languageCode === lang && t.kind === 'asr');
  const anyLang = tracks.find((t) => t.languageCode === lang);
  const track = manual || auto || anyLang || tracks[0];

  const trackLang = track.languageCode || lang;
  const isAutoGenerated = track.kind === 'asr';

  for (const fmt of ['json3', undefined, 'srv3']) {
    try {
      let captionUrl = track.baseUrl;
      if (fmt && !captionUrl.includes('fmt=')) {
        captionUrl += (captionUrl.includes('?') ? '&' : '?') + `fmt=${fmt}`;
      }

      const resp = await fetch(captionUrl, {
        headers: {
          'User-Agent': BROWSER_UA,
          'Accept-Language': lang,
          Cookie: CONSENT_COOKIE,
        },
      });

      if (!resp.ok) continue;
      const text = await resp.text();
      const lines = parseCaptionData(text);
      if (lines.length > 0) {
        return { lines, language: trackLang, isAutoGenerated };
      }
    } catch {
      continue;
    }
  }

  return null;
}

function parseCaptionData(data) {
  // JSON3 format
  if (data.trimStart().startsWith('{')) {
    try {
      const json = JSON.parse(data);
      const events = json.events || [];
      const lines = [];
      let id = 0;
      for (const event of events) {
        const text = (event.segs || []).map((s) => s.utf8).join('').trim();
        if (!text || text === '\n') continue;
        lines.push({
          id: `yt_${++id}`,
          start: event.tStartMs / 1000,
          end: (event.tStartMs + event.dDurationMs) / 1000,
          text,
        });
      }
      if (lines.length > 0) return lines;
    } catch { /* not JSON */ }
  }

  // XML format
  if (data.includes('<')) {
    return parseXml(data);
  }

  return [];
}

function parseXml(xml) {
  const lines = [];
  let id = 0;
  let match;

  // srv3 format
  const pRegex = /<p\s+t="(\d+)"\s+d="(\d+)"[^>]*>([\s\S]*?)<\/p>/g;
  while ((match = pRegex.exec(xml)) !== null) {
    const startMs = parseInt(match[1], 10);
    const durMs = parseInt(match[2], 10);
    const inner = match[3];
    let text = '';
    const sRegex = /<s[^>]*>([^<]*)<\/s>/g;
    let sMatch;
    while ((sMatch = sRegex.exec(inner)) !== null) {
      text += sMatch[1];
    }
    if (!text) text = inner.replace(/<[^>]+>/g, '');
    text = decodeEntities(text).trim();
    if (text) {
      lines.push({
        id: `yt_${++id}`,
        start: startMs / 1000,
        end: (startMs + durMs) / 1000,
        text,
      });
    }
  }
  if (lines.length > 0) return lines;

  // Classic format
  const classicRegex =
    /<text\s+start="([\d.]+)"\s+dur="([\d.]+)"[^>]*>([\s\S]*?)<\/text>/g;
  while ((match = classicRegex.exec(xml)) !== null) {
    const start = parseFloat(match[1]);
    const dur = parseFloat(match[2]);
    const text = decodeEntities(match[3]).replace(/\n/g, ' ').trim();
    if (text) {
      lines.push({
        id: `yt_${++id}`,
        start,
        end: start + dur,
        text,
      });
    }
  }
  return lines;
}

function decodeEntities(str) {
  return str
    .replace(/&quot;/g, '"')
    .replace(/&amp;/g, '&')
    .replace(/&#39;/g, "'")
    .replace(/&apos;/g, "'")
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/\\u0026/g, '&')
    .replace(/\\"/g, '"');
}

/**
 * Extract a complete JSON object from a string, properly handling
 * string literals and escape sequences.
 */
function extractJsonObject(src, startIdx) {
  let depth = 0;
  let inString = false;
  let escaped = false;

  for (let i = startIdx; i < src.length; i++) {
    const ch = src[i];
    if (escaped) { escaped = false; continue; }
    if (inString) {
      if (ch === '\\') escaped = true;
      else if (ch === '"') inString = false;
      continue;
    }
    if (ch === '"') inString = true;
    else if (ch === '{') depth++;
    else if (ch === '}') { depth--; if (depth === 0) return src.slice(startIdx, i + 1); }
  }
  return null;
}

