// ── Transcript endpoint ────────────────────────────────────────
app.get('/api/transcript', async (req, res) => {
  const videoId = req.query.videoId;
  const lang = (req.query.lang || 'en').toString();

  if (!videoId) {
    return res.status(400).json({ error: 'Missing videoId parameter' });
  }

  console.log(`[${ts()}] Fetching transcript: ${videoId} (lang=${lang})`);
  const startTime = Date.now();

  // Strategy 1: youtube-transcript npm package (uses your residential IP)
  try {
    const result = await YoutubeTranscript.fetchTranscript(videoId, { lang });

    if (result && result.length > 0) {
      const lines = result.map((item, i) => ({
        id: `yt_${i + 1}`,
        start: item.offset > 1000 ? item.offset / 1000 : item.offset,
        end:
          (item.offset > 1000 ? item.offset / 1000 : item.offset) +
          (item.duration > 1000 ? item.duration / 1000 : item.duration),
        text: item.text,
      }));

      const elapsed = Date.now() - startTime;
      console.log(
        `[${ts()}] OK — ${lines.length} lines in ${elapsed}ms (${result[0]?.lang || lang})`,
      );

      return res.json({
        lines,
        language: result[0]?.lang || lang,
        isAutoGenerated: false,
        source: 'local-proxy (youtube-transcript)',
      });
    }
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    console.warn(`[${ts()}] youtube-transcript failed: ${msg}`);
  }

  // Strategy 2: InnerTube API directly (also uses residential IP)
  try {
    const innerTubeResult = await fetchViaInnerTube(videoId, lang);
    if (innerTubeResult) {
      const elapsed = Date.now() - startTime;
      console.log(
        `[${ts()}] OK (InnerTube) — ${innerTubeResult.lines.length} lines in ${elapsed}ms`,
      );
      innerTubeResult.source = 'local-proxy (InnerTube)';
      return res.json(innerTubeResult);
    }
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    console.warn(`[${ts()}] InnerTube failed: ${msg}`);
  }

  // Strategy 3: Web page scraping (also uses residential IP)
  try {
    const webResult = await fetchViaWebPage(videoId, lang);
    if (webResult) {
      const elapsed = Date.now() - startTime;
      console.log(
        `[${ts()}] OK (WebPage) — ${webResult.lines.length} lines in ${elapsed}ms`,
      );
      webResult.source = 'local-proxy (WebPage)';
      return res.json(webResult);
    }
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    console.warn(`[${ts()}] WebPage failed: ${msg}`);
  }

  const elapsed = Date.now() - startTime;
  console.log(`[${ts()}] FAIL — no transcript found for ${videoId} (${elapsed}ms)`);
  return res.status(404).json({ error: 'No transcript available for this video' });
});

