import type { TranscriptLine } from '../types';

const PROXY_BASE = 'https://proxy.echo-learn.uk';

interface BilibiliSubtitleLine {
  from: number;
  to: number;
  content: string;
  location?: number;
}

interface TranscriptFetchResult {
  lines: TranscriptLine[];
  language: string;
  isAutoGenerated: boolean;
}

/**
 * Fetch transcript/subtitles for a Bilibili video.
 *
 * Steps:
 * 1. Get video info (cid, title) from Bilibili's web-interface API
 * 2. Get subtitle track URLs from the player API
 * 3. Fetch and parse the subtitle JSON
 */
export async function fetchBilibiliTranscript(
  bvid: string,
  _lang?: string,
): Promise<TranscriptFetchResult> {
  // Step 1: Get video info (cid)
  const videoInfo = await fetchBilibiliVideoInfo(bvid);
  if (!videoInfo) {
    throw new Error('Could not fetch Bilibili video info');
  }

  const { cid } = videoInfo;

  // Step 2: Get subtitle tracks
  const subtitles = await fetchBilibiliSubtitles(bvid, cid);

  if (subtitles.length === 0) {
    throw new Error(
      'No subtitles available for this video. ' +
      'The video may not have CC subtitles. You can import a transcript manually.',
    );
  }

  // Prefer Chinese subtitles, then English, then first available
  let selectedSub = subtitles.find((s) => s.lang === 'zh-CN' || s.lang === 'zh-Hans');
  if (!selectedSub) selectedSub = subtitles.find((s) => s.lang === 'en' || s.lang === 'en-US');
  if (!selectedSub) selectedSub = subtitles[0];

  // Step 3: Fetch subtitle content
  const lines = await fetchSubtitleContent(selectedSub.url);

  return {
    lines,
    language: selectedSub.lang,
    isAutoGenerated: selectedSub.isAutoGenerated,
  };
}

/**
 * Fetch basic video info (cid, title, pages) from Bilibili.
 */
async function fetchBilibiliVideoInfo(
  bvid: string,
): Promise<{ cid: number; title: string; pages: Array<{ cid: number; page: number; part: string }> } | null> {
  const url = `${PROXY_BASE}/https://api.bilibili.com/x/web-interface/view?bvid=${bvid}`;

  const resp = await fetch(url);
  if (!resp.ok) throw new Error(`Bilibili API error: ${resp.status}`);

  const data = await resp.json();
  if (data.code !== 0) {
    throw new Error(`Bilibili API error: ${data.message || 'unknown'}`);
  }

  const info = data.data;
  return {
    cid: info.cid,
    title: info.title,
    pages: (info.pages || []).map((p: any) => ({
      cid: p.cid,
      page: p.page,
      part: p.part,
    })),
  };
}

interface SubtitleTrack {
  url: string;
  lang: string;
  langDoc: string;
  isAutoGenerated: boolean;
}

/**
 * Fetch subtitle track list from Bilibili's player API.
 */
async function fetchBilibiliSubtitles(
  bvid: string,
  cid: number,
): Promise<SubtitleTrack[]> {
  const url = `${PROXY_BASE}/https://api.bilibili.com/x/player/v2?bvid=${bvid}&cid=${cid}`;

  const resp = await fetch(url);
  if (!resp.ok) throw new Error(`Bilibili player API error: ${resp.status}`);

  const data = await resp.json();
  if (data.code !== 0) {
    throw new Error(`Bilibili player API error: ${data.message || 'unknown'}`);
  }

  const subtitleInfo = data.data?.subtitle;
  if (!subtitleInfo || !subtitleInfo.subtitles) return [];

  return subtitleInfo.subtitles.map((s: any) => ({
    url: s.subtitle_url.startsWith('http') ? s.subtitle_url : `https:${s.subtitle_url}`,
    lang: s.lan || 'unknown',
    langDoc: s.lan_doc || s.lan || 'unknown',
    isAutoGenerated: !!(s.type && s.type === 'ai'),
  }));
}

/**
 * Fetch and parse subtitle JSON content into TranscriptLine[].
 *
 * Bilibili subtitle format:
 * { body: [{ from: 1.2, to: 3.4, content: "text", location: 2 }] }
 */
async function fetchSubtitleContent(subtitleUrl: string): Promise<TranscriptLine[]> {
  // Route through proxy to avoid CORS
  const proxiedUrl = `${PROXY_BASE}/${subtitleUrl}`;

  const resp = await fetch(proxiedUrl);
  if (!resp.ok) throw new Error(`Subtitle fetch error: ${resp.status}`);

  const data = await resp.json();
  const body: BilibiliSubtitleLine[] = data.body || [];

  if (body.length === 0) return [];

  return body.map((line, idx) => ({
    start: line.from,
    end: line.to,
    text: line.content.trim(),
    id: `bili_${idx}`,
  }));
}

/**
 * Fetch video title from Bilibili.
 * Uses the web-interface API (no auth required).
 */
export async function getBilibiliVideoTitle(
  bvid: string,
): Promise<{ title: string; ownerName: string } | null> {
  try {
    const url = `${PROXY_BASE}/https://api.bilibili.com/x/web-interface/view?bvid=${bvid}`;
    const resp = await fetch(url);
    if (!resp.ok) return null;
    const data = await resp.json();
    if (data.code !== 0) return null;
    return {
      title: data.data.title,
      ownerName: data.data.owner?.name || '',
    };
  } catch {
    return null;
  }
}
